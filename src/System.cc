// System class의 생성자, argv[1] = path_to_vocabulary = Vocabulary/ORBvoc.txt, argv[2] = path_to_settings = Examples/Monocular/KITTIX.yaml
System::System(const string &strVocFile, const string &strSettingsFile, const eSensor sensor,
               const bool bUseViewer):mSensor(sensor), mpViewer(static_cast<Viewer*>(NULL)), mbReset(false),mbActivateLocalizationMode(false),
        mbDeactivateLocalizationMode(false) // 초기화
{
    // Output welcome message
    cout << endl <<
    "ORB-SLAM2 Copyright (C) 2014-2016 Raul Mur-Artal, University of Zaragoza." << endl <<
    "This program comes with ABSOLUTELY NO WARRANTY;" << endl  <<
    "This is free software, and you are welcome to redistribute it" << endl <<
    "under certain conditions. See LICENSE.txt." << endl << endl;

    cout << "Input sensor was set to: ";

    if(mSensor==MONOCULAR)
        cout << "Monocular" << endl;
    else if(mSensor==STEREO)
        cout << "Stereo" << endl;
    else if(mSensor==RGBD)
        cout << "RGB-D" << endl;

    //Check settings file
    cv::FileStorage fsSettings(strSettingsFile.c_str(), cv::FileStorage::READ);
    if(!fsSettings.isOpened())
    {
       cerr << "Failed to open settings file at: " << strSettingsFile << endl;
       exit(-1);
    }


    //Load ORB Vocabulary
    cout << endl << "Loading ORB Vocabulary. This could take a while..." << endl;

    mpVocabulary = new ORBVocabulary();
    bool bVocLoad = mpVocabulary->loadFromTextFile(strVocFile);
    if(!bVocLoad)
    {
        cerr << "Wrong path to vocabulary. " << endl;
        cerr << "Falied to open at: " << strVocFile << endl;
        exit(-1);
    }
    cout << "Vocabulary loaded!" << endl << endl;

    //Create KeyFrame Database
    mpKeyFrameDatabase = new KeyFrameDatabase(*mpVocabulary);

    //Create the Map
    mpMap = new Map();

    //Create Drawers. These are used by the Viewer
    mpFrameDrawer = new FrameDrawer(mpMap);
    mpMapDrawer = new MapDrawer(mpMap, strSettingsFile);

    //Initialize the Tracking thread
    //(it will live in the main thread of execution, the one that called this constructor)
    mpTracker = new Tracking(this, mpVocabulary, mpFrameDrawer, mpMapDrawer,
                             mpMap, mpKeyFrameDatabase, strSettingsFile, mSensor);

    //Initialize the Local Mapping thread and launch
    mpLocalMapper = new LocalMapping(mpMap, mSensor==MONOCULAR);
    mptLocalMapping = new thread(&ORB_SLAM2::LocalMapping::Run,mpLocalMapper);

    //Initialize the Loop Closing thread and launch
    mpLoopCloser = new LoopClosing(mpMap, mpKeyFrameDatabase, mpVocabulary, mSensor!=MONOCULAR);
    mptLoopClosing = new thread(&ORB_SLAM2::LoopClosing::Run, mpLoopCloser);

    //Initialize the Viewer thread and launch
    if(bUseViewer)
    {
        mpViewer = new Viewer(this, mpFrameDrawer,mpMapDrawer,mpTracker,strSettingsFile);
        mptViewer = new thread(&Viewer::Run, mpViewer);
        mpTracker->SetViewer(mpViewer);
    }

    //Set pointers between threads
    mpTracker->SetLocalMapper(mpLocalMapper);
    mpTracker->SetLoopClosing(mpLoopCloser);

    mpLocalMapper->SetTracker(mpTracker);
    mpLocalMapper->SetLoopCloser(mpLoopCloser);

    mpLoopCloser->SetTracker(mpTracker);
    mpLoopCloser->SetLocalMapper(mpLocalMapper);
}

// left image, right image, timestamp를 input으로 하며, world to camera coordinate의 transformation을 output으로 하는 함수
cv::Mat System::TrackStereo(const cv::Mat &imLeft, const cv::Mat &imRight, const double &timestamp)
{
    if(mSensor!=STEREO) // enumerator를 객체화하여 변수로 정의했던 mSensor가 STEREO가 아니라면,
    {
        cerr << "ERROR: you called TrackStereo but input sensor was not set to STEREO." << endl;
        exit(-1); // exit() -> 프로그램 단위로 제어
    }

    // Check mode change -> FLAG만 변경
    // Localization mode or SLAM mode
    // Critical Section
    {
        // lock_guard는 scope 단위로 자동적으로 mutex를 해제한다. -> unique_lock도 비슷하게 동작한다. 하지만 가장 큰 차이점은 unique_lock은 move가 가능하다는 점이다.
        // 함수의 매개변수나, return 값으로 받을 수도 있다. -> 다른 함수에서도 lock 기능을 이용할 수 있다.
        // mutex에 lock을 걸어주면, mutex lock을 획득한 오직 하나의 thread만 접근 가능하다.
        // mutex -> 다른 쓰레드가 공유자원에 접근을 하지 못하게 만드는 동기화 객체이다.
        // 공유 자원에 접근하기 위해서는 mutex에 대한 잠금을 획득하고, 접근 후에는 잠금을 해제해야 한다.
        // lock은 이러한 mutex를 기반으로 잠글 수 있는 기능을 캡슐화한 객체이다. 쉽게 생각하면 자물쇠가 lock이고, 
        // 자물쇠의 열쇠 구멍을 mutex라고 생각할 수 있다. 이러한 객체들은 개발자가 쉽고 간편하게 공유자원의 동시접근을 막을 수 있도록 도와준다.
        unique_lock<mutex> lock(mMutexMode); // unique_lock class의 객체인 lock은 mutex 객체인 mMutexMode를 소유한다.
        // ***다른 class의 공유 자원에 접근하려고 할 때, 무조건 lock 걸기***
        // 기본적으로 생성과 동시에 lock이 걸리고 소멸시에 unlock되지만 그 밖에도 옵션을 통해 생성시 lock을 안 시키고 따로 특정 시점에 lock을 걸 수 있다.
        // Q. mMutexMode에 대하여 lock을 걸었다면, 해당 thread에서는 mMutexMode에 접근할 수 있고, 다른 thread에서는 mMutexMode에 접근할 수 없다.
        // Localization mode가 활성화 된다면, -> GUI에서 mode 활성화 -> localization thread만 활성화
        if(mbActivateLocalizationMode) // FLAG
        {
            // Localization mode가 활성화되면, Local Mapping thread를 중지하라는 메시지 요청과 관련된 flag를 변경한다.
            // ORB_SLAM2::LocalMapping::RequestStop();
            mpLocalMapper->RequestStop(); // mpLocalMapper는 LocalMapping class를 pointer로 instanciation -> 포인터로 접근할 수 있다.
            // Q. mpLocalMapper를 LocalMapping class의 포인터로 받아오는 이유 -> this pointer처럼 이용하기 위해서? 혹은 주소를 옮겨, 변수가 복사되는 것을 방지하기 위해서?
            // mbStopRequested = true;
            // mbAbortBA = true;

            // Wait until Local Mapping has effectively stopped
            while(!mpLocalMapper->isStopped()) // mbStopped = False -> Local Mapping이 중지되지 않았다면,
            {
                // usleep()이 선언되어 있는 해당 thread가 대기한다. -> tracking thread가 대기한다.
                usleep(1000); // 마이크로초 간격으로 실행을 일시중지한다. -> Q. Local Mapping이 완전히 중지되어, mbStopped Flag가 True로 바뀔 때까지 대기한다.
            }

            // Local Mapping thread가 비활성화되면, InformOnlyTracking 함수에서 true를 인수로 받아, mbOnlyTracking flag를 변경한다.
            mpTracker->InformOnlyTracking(true); // mbOnlyTracking = true
            mbActivateLocalizationMode = false; // mbActivateLocalizationMode flag를 default 값으로 변경한다.
        }
        // Localization mode가 비활성화 된다면, -> SLAM mode
        if(mbDeactivateLocalizationMode) // FLAG
        {
            // Localization thread가 비활성화되면, InformOnlyTracking 함수에서 false를 인수로 받아, mbOnlyTracking flag를 변경한다. 
            mpTracker->InformOnlyTracking(false); // mbOnlyTracking = false -> Tracking thread를 실행하지 말라는 것이 아니라, Local Mapping thread도 같이 실행하라는 의미 같다.
            mpLocalMapper->Release(); // Q. 또 다른 새로운 keyframe을 받기 위해, 기존의 new keyframe list를 삭제
            mbDeactivateLocalizationMode = false; // mbDeactiveLocalizationMode flag를 default 값으로 변경한다.
        }
    }

    // Check reset -> tracking을 수행하기 전, system reset
    // Critical Section
    {
    unique_lock<mutex> lock(mMutexReset); // unique_lock class의 객체인 lock이 mutex 객체인 mMutexReset을 소유하고 있다.
    
    if(mbReset) // mbReset = true -> reset 해라.
    {
        mpTracker->Reset(); // system의 모든 로그를 삭제한다.
        mbReset = false; // mbReset flag를 default 값으로 변경한다.
    }
    }

    // Tracking
    cv::Mat Tcw = mpTracker->GrabImageStereo(imLeft,imRight,timestamp);

    unique_lock<mutex> lock2(mMutexState);
    mTrackingState = mpTracker->mState;
    mTrackedMapPoints = mpTracker->mCurrentFrame.mvpMapPoints;
    mTrackedKeyPointsUn = mpTracker->mCurrentFrame.mvKeysUn;
    return Tcw; // world to camera coordinate
}

cv::Mat System::TrackRGBD(const cv::Mat &im, const cv::Mat &depthmap, const double &timestamp)
{
    if(mSensor!=RGBD)
    {
        cerr << "ERROR: you called TrackRGBD but input sensor was not set to RGBD." << endl;
        exit(-1);
    }    

    // Check mode change
    {
        unique_lock<mutex> lock(mMutexMode);
        if(mbActivateLocalizationMode)
        {
            mpLocalMapper->RequestStop();

            // Wait until Local Mapping has effectively stopped
            while(!mpLocalMapper->isStopped())
            {
                usleep(1000);
            }

            mpTracker->InformOnlyTracking(true);
            mbActivateLocalizationMode = false;
        }
        if(mbDeactivateLocalizationMode)
        {
            mpTracker->InformOnlyTracking(false);
            mpLocalMapper->Release();
            mbDeactivateLocalizationMode = false;
        }
    }

    // Check reset
    {
    unique_lock<mutex> lock(mMutexReset);
    if(mbReset)
    {
        mpTracker->Reset();
        mbReset = false;
    }
    }

    cv::Mat Tcw = mpTracker->GrabImageRGBD(im,depthmap,timestamp);

    unique_lock<mutex> lock2(mMutexState);
    mTrackingState = mpTracker->mState;
    mTrackedMapPoints = mpTracker->mCurrentFrame.mvpMapPoints;
    mTrackedKeyPointsUn = mpTracker->mCurrentFrame.mvKeysUn;
    return Tcw;
}

